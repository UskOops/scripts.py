name = input("What is your name? ").strip() #strip() remove espaços em branco
print('analyzing your name...')
print(f'your name in upper case is:', name.upper())
print(f'your name in lower case is:', name.lower())
print(f'your name has {len(name)} characters') #len() conta o número de caracteres
print(f'your first name has: letters', name.find(' ')) #find() procura o primeiro caracter




